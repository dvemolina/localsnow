	<h1 class="title2">About Local Snow</h1>

	<section>
		<h2 class="title3">Why This Exists</h2>
		<p>
			I'm a ski instructor. I've worked at resorts across Spain for years, and I got tired of
			watching booking platforms take 15-20% commissions from every lesson. These platforms add no
			real value - they don't teach the lessons, handle the scheduling, or manage the risk. They
			just list profiles and take a cut.
		</p>
		<p>
			Instructors either absorb that cost (earning less) or pass it to clients (making lessons more
			expensive). Either way, everyone loses except the platform.
		</p>
		<p>So I built something different.</p>
	</section>

	<section>
		<h2 class="title3">What Local Snow Is</h2>
		<p>
			Local Snow is a free directory of ski and snowboard instructors across Spain. Clients search
			for instructors by resort and sport, see real profiles with qualifications and pricing, and
			contact instructors directly. No booking fees. No commissions. No middlemen.
		</p>
		<p>
			Instructors pay a small fee (â‚¬5) to access client contact information when they receive a
			booking request. That's it. The lead fee covers server costs and keeps the platform running.
			It's not a business model - it's cost recovery.
		</p>
	</section>

	<section>
		<h2 class="title3">The Verification Badge</h2>
		<p>
			I manually check every instructor who applies for verification. I look at certifications,
			qualifications, and professional credentials. The badge means I've reviewed their documents -
			it doesn't guarantee they're the best instructor in the world, but it confirms they're
			legitimate and qualified.
		</p>
		<p>
			I'm not trying to build a brand or scale this into some venture-backed startup. I just want a
			fair system where instructors keep what they earn and clients pay what lessons actually cost.
		</p>
	</section>

	<section>
		<h2 class="title3">Why Spain First</h2>
		<p>
			I work here. I know the resorts, I know instructors, and I understand the local ski industry.
			Starting focused made more sense than trying to list every resort worldwide on day one.
		</p>
		<p>
			If this works in Spain, maybe it expands to other countries. If it doesn't, at least I tried
			to fix a problem I saw every season.
		</p>
	</section>

